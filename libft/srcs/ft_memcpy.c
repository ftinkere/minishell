#include <stddef.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (dest == NULL && src == NULL)
		return (NULL);
	while (i++ < n)
		((unsigned char *)dest)[i - 1] = ((unsigned char *)src)[i - 1];
	return (dest);
}
